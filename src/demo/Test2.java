package demo;

public class Test2 {

	public static void main(String[] args) {

int x=5;
System.out.println(x++);//(5+1)
System.out.println(x);//6
System.out.println("                 ");

System.out.println(++x);//(1+6)
System.out.println(x);//7
System.out.println("                 ");

System.out.println(x--);//(7-1)
System.out.println(x);//6
System.out.println("                 ");

System.out.println(--x);//(1-6)
System.out.println(x);//5


	}

}
